        <p>En Pinterest, nuestra misión consiste en inspirar a las personas a crear la vida que desean, y este es nuestro norte a la hora de redactar nuestras políticas de contenido y velar por su cumplimiento.</p>
        <br>
        <p>No todo el contenido que hay en Internet es inspirador, por lo que tenemos límites para determinar lo que es aceptable en Pinterest y lo que no está permitido. Nuestras prácticas de moderación están en constante evolución para mantenerse a la par de los nuevos comportamientos y tendencias, y para crear un espacio más positivo en Internet para los usuarios de nuestra plataforma. Seguimos invirtiendo mucho en medidas como la tecnología de aprendizaje automático para combatir el contenido en el que se infringen las políticas en Pinterest. También trabajamos con organizaciones y expertos externos para orientar nuestras políticas y prácticas de moderación de contenido.</p>
        <br>
        <p>Empezamos a publicar un informe de transparencia semestral en el 2013, y comenzamos a ampliarlo a partir de 2020 para incluir más información sobre las medidas que tomamos para ratificar nuestras Directrices para la comunidad. En cada informe, encontrarás información sobre nuestros esfuerzos para mantener nuestra plataforma segura e inspiradora, como la cantidad de desactivaciones de Pines y cuentas. Los informes también incluyen datos sobre el volumen de información y las solicitudes de desactivación que recibimos por parte de las fuerzas de seguridad y las entidades gubernamentales.</p>
