        <h2 class="text-3xl font-semibold text-rose-800 mt-10 mb-4">¿Qué pasa si mi contenido se elimina a partir de un reporte de infracción de derechos de autor?</h2>
        <div class="text-gray-700 mb-10 leading-relaxed space-y-4">
  <p>
    Si recibiste una notificación de que tu contenido se eliminó a partir de un reporte de infracción de derechos de autor,
    significa que el propietario de esos derechos presentó un reporte completo y válido donde nos solicita que lo eliminemos.
    Si quieres que te enviemos la información del reporte, envía un correo electrónico a 
    <a href="mailto:copyright@pinterest.com" class="text-rose-700 hover:underline">copyright@pinterest.com</a>
    para informarnos esto (es posible que eliminemos cierta información de contacto personal).
  </p>

  <p>Si se elimina contenido de tu cuenta debido a que recibiste demasiados informes de infracción de derechos de autor,es posible que pierdas la capacidad de guardar Pines en Pinterest o que desactivemos tu cuenta del todo.</p>

  <p>Si crees que cometimos un error al eliminar tu Pin, puedes presentar una <span class="font-semibold">impugnación</span>. Cuando recibamos una impugnación válida, restableceremos el contenido y eliminaremos el reclamo del registro de tu cuenta.</p>

  <p class="text-sm text-gray-600 italic"><span class="font-semibold not-italic">Nota:</span> las impugnaciones de mala fe o fraudulentas pueden tener posibles consecuencias legales y financieras.Antes de enviar una impugnación, asegúrate de que crees de buena fe que eliminamos el contenido por error y que entiendes las repercusiones de los envíos falsos.</p>
</div>
  <h2 class="text-3xl font-semibold text-rose-800 mt-10 mb-4">Cómo presentar una impugnación</h2>
<div class="text-gray-700 mb-10 leading-relaxed space-y-4">
  <p>
    Si tu contenido se elimina como resultado de un reporte de infracción de derechos de autor, recibirás instrucciones sobre cómo presentar una impugnación en la notificación que te enviaremos por correo electrónico. Ten en cuenta que solo debes presentar una impugnación si crees que el contenido se eliminó por error. 
  </p>

  <p>
    Cuando recibamos una impugnación válida, la enviaremos a la parte que denunció el contenido. La información que va a recibir incluirá tu información de contacto, la cual puede usar para comunicarse contigo de forma directa o para tomar medidas adicionales que considere oportunas.
  </p>

  <p>
    Si le enviamos la impugnación a la parte que denunció el contenido y esta no nos informa que inició acciones legales en un tribunal o ante la Junta de Reclamos de Derechos de Autor para solicitar una resolución para que el contenido permanezca desactivado, restableceremos el contenido. Este proceso puede demorar un máximo de 14 días hábiles.
  </p>
</div>
<h2 class="text-3xl font-semibold text-rose-800 mt-10 mb-4">Infracciones recurrentes</h2>
<div class="text-gray-700 mb-10 leading-relaxed space-y-4">
  <p>
    En circunstancias adecuadas y a nuestra entera discreción, nuestra política establece la desactivación o eliminación de las cuentas de personas que infrinjan o sean acusadas reiteradamente de infringir derechos de autor u otros derechos de propiedad intelectual. Los usuarios cuyos Pines se eliminen debido a una notificación de infracción de derechos de autor recibirán penalizaciones que se computarán dentro de la política de infracciones recurrentes de Pinterest.
  </p>
</div>
<h2 class="text-3xl font-semibold text-rose-800 mt-10 mb-4">Apela una penalización por derechos de autor</h2>
<div class="text-gray-700 mb-10 leading-relaxed space-y-4">
  <p>
    Si uno de tus Pines se eliminó a partir de un reporte de infracción de derechos de autor y no puedes (o no quieres) presentar las alegaciones necesarias para presentar una impugnación, pero crees que las circunstancias justifican la eliminación de la penalización, puedes presentar una apelación para especificar los detalles de las circunstancias. Revisaremos la apelación y tomaremos las medidas correspondientes.
  </p>
</div>
</div>
<h2 class="text-3xl font-semibold text-rose-800 mt-10 mb-4">Portal de reclamación de contenido</h2>
<div class="text-gray-700 mb-10 leading-relaxed space-y-4">
  <p>
    Además de nuestros canales de reporte de derechos de autor, Pinterest también ofrece el Portal de Reclamación de Contenido, una herramienta que permite a los creadores reclamar su contenido y decidir si y cómo quieren que aparezca en Pinterest.
  </p>
  <p>
    Cualquier persona que posea derechos de autor exclusivos de contenido puede solicitar acceso al Portal de reclamación de contenido. Revisamos las solicitudes para ver si el titular de los derechos es apto para usar la herramienta. Si se aprueba la solicitud, el titular de los derechos podrá cargar su contenido original e informarnos si quiere que dicho contenido aparezca en Pinterest o no. La medida de ejecución que seleccione se usará tanto para coincidencias existentes como para coincidencias futuras que se agreguen a Pinterest.
  </p>
  <p>
    Para obtener más información, visita el artículo del Portal de reclamación de contenido.
  </p>
