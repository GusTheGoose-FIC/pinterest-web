        <h2 class="text-3xl font-semibold text-rose-800 mt-10 mb-4">Copyright en Pinterest</h2>
        <p class="text-gray-700 mb-10 text-rose-800">Pinterest respeta los derechos de propiedad intelectual de los demás y esperamos que los usuarios también lo hagan.<br>
        De conformidad con la Ley de Derechos de Autor del Milenio Digital (DMCA, por sus siglas en inglés), que puedes leer en el sitio web de la Oficina del Derecho de Autor de Estados Unidos, responderemos rápidamente a los reclamos de infracción de derechos de autor en Pinterest que se informen a nuestro agente de derechos de autor designado, el cual se identifica a continuación.<br>
      Si eres propietario de derechos de autor o estás autorizado a actuar en su nombre, puedes denunciar presuntas infracciones de derechos de autor en Pinterest. Para hacer esto, deberás completar un informe de derechos de autor y enviarlo a nuestro agente de derechos de autor designado.<br>
    Cuando recibamos el reporte, tomaremos las medidas correspondientes, que pueden incluir desactivar o eliminar el acceso al contenido denunciado en Pinterest.<br>
  Si desactivamos o eliminamos el acceso a contenido en respuesta a una denuncia de infracción de derechos de autor, es posible que se lo notifiquemos a la persona que lo guardó en Pinterest para que pueda presentar una impugnación. También es posible que enviemos una copia completa del aviso a otras personas, según corresponda.<br>
</p>

        <h2 class="text-3xl font-semibold text-rose-800 mt-10 mb-4">Presenta una denuncia de infracción de derechos de autor</h2>
        <p class="text-gray-700 mb-10">Para hacerlo, solo debes completar el formulario de denuncias de derechos de autor.<br>
          <div class="text-gray-700 mb-10 leading-relaxed space-y-4">
  <p>
    Para hacerlo, solo debes completar el formulario de denuncias de derechos de autor.
    <br>
    Si prefieres no usar este formulario, envía esta información a nuestro agente de copyright designado:
  </p>

  <ol class="list-decimal list-inside space-y-3">
    <li>
      <span class="font-semibold">Identificación del contenido</span> con derechos de autor que crees que se infringió.
    </li>

    <li>
      <span class="font-semibold">Identificación del contenido en Pinterest</span> que alegas que constituye una infracción de tus derechos, incluida la información que necesitamos para poder encontrarlo, como la URL al contenido en Pinterest.
    </li>

    <li>
      Tu dirección postal, número de teléfono y dirección de correo electrónico.
    </li>

    <li>
      Estas dos declaraciones en el cuerpo del aviso:
      <ul class="list-disc list-inside pl-6 mt-2 space-y-2 text-rose-700">
        <li>
          "Por la presente, declaro que, a mi leal saber y entender, el uso disputado del material con derechos de autor, o una referencia o enlace a dicho material, no está autorizado por el propietario de los derechos, su agente ni la ley (por ejemplo, como un uso razonable)."
        </li>
        <li>
          "Por la presente, declaro que la información que aparece en este aviso es precisa y, bajo pena de perjurio, que soy el propietario, o que estoy autorizado para actuar en nombre del propietario de los derechos de autor o de un derecho exclusivo bajo los derechos de autor supuestamente infringidos."
        </li>
      </ul>
